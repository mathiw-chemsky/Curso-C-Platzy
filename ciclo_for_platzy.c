//Ciclo for (platzy Control de flujo C) cuando
//conocemos el tamaño de lo que recorremos, se usa con arrays
#include <stdio.h>

int main(){
	//Declaración de variable local
	int n = 30;
	int i;

	//ciclo for
	for(i = 0; i < n; i++)
		printf("El valor actual de i es: %d \n", i);

	return 0;
}
